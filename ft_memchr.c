#include <stddef.h>

void	*ft_memchr(const void *str, int c, size_t n)
{
	unsigned char	*s;

	s = (unsigned char *) str;
	while (*s && n > 0)
	{
		if (*s == c)
		{
			return ((void *) s);
		}
		s++;
		n--;
	}
	return (0);
}
